import UIKit

class Day23_CaseOfTheString: NSObject {
    
    let myString = "case of the STRING"
    
    func printString() {
        print("Capitalized String : " + myString.capitalizedString)
        print("Lowercase String : " + myString.lowercaseString)
        print("Uppercase String : " + myString.uppercaseString)
    }
    
}
